int n = int.Parse(Console.ReadLine()!);

if (n >= 620) Console.WriteLine("Red");
else if (n >= 590) Console.WriteLine("Orange");
else if (n >= 570) Console.WriteLine("Yellow");
else if (n >= 495) Console.WriteLine("Green");
else if (n >= 450) Console.WriteLine("Blue");
else if (n >= 425) Console.WriteLine("Indigo");
else Console.WriteLine("Violet");